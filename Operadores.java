package pruebasIniciales;

public class Operadores {

	public static void main(String[] args) {
		
		// Para hacer uso de Operadores y repasar variables y constantes ademas de concatenaciones
		// Vamos a convertir 10 centimetros a pulgadas 
		
		int a = 10;
		final double b = 2.54; // declaro como constante y como decimal
		double c = a / b ;// divido los centimetros entre lo que equivale 1 pulgada en centimetros
		System.out.println(a + " centimetros son "+ c +" pulgadas");
		System.out.print("Una pulgada equivale a "+ b +"centimetros");
		
	}

}
